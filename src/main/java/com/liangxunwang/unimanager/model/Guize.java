public class Guize {
    private String guize_id;
    private String guize_time;
    private String guize_number;
    private String guize_table;

    public String getGuize_id() {
        return guize_id;
    }

    public void setGuize_id(String guize_id) {
        this.guize_id = guize_id;
    }

    public String getGuize_time() {
        return guize_time;
    }

    public void setGuize_time(String guize_time) {
        this.guize_time = guize_time;
    }

    public String getGuize_number() {
        return guize_number;
    }

    public void setGuize_number(String guize_number) {
        this.guize_number = guize_number;
    }

    public String getGuize_table() {
        return guize_table;
    }

    public void setGuize_table(String guize_table) {
        this.guize_table = guize_table;
    }
}
